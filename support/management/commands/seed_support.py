class Command(BaseCommand):
    help = 'Создаёт базовые FAQ для Центра поддержки'

    def handle(self, *args, **options):
        created_cats = 0
        created_arts = 0
        for i, cat_data in enumerate(DEFAULT_FAQS):
            cat, c = FAQCategory.objects.get_or_create(
                slug=cat_data['slug'],
                defaults={
                    'name': cat_data['name'],
                    'icon': cat_data['icon'],
                    'order': i,
                },
            )
            if c:
                created_cats += 1
            for j, (title, body) in enumerate(cat_data['articles']):
                slug = slugify(title, allow_unicode=True)[:50] or f'faq-{cat.slug}-{j}'
                _, a = FAQArticle.objects.get_or_create(
                    slug=slug,
                    defaults={
                        'category': cat,
                        'title': title,
                        'body': body,
                        'order': j,
                        'is_published': True,
                    },
                )
                if a:
                    created_arts += 1
        self.stdout.write(self.style.SUCCESS(
            f'FAQ: +{created_cats} категорий, +{created_arts} статей'
        ))
